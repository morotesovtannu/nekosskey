export function nojaize(text: string): string {
	return text
		// ja-JP
		.replace(/だ$/gm, 'じゃ')
		.replace(/(だ{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'じゃ')
		/*.replace(/(だ{1})(?=、)/gm, 'じゃ')
		.replace(/(だ{1})(?=\！)/gm, 'じゃ')
		.replace(/(だ{1})(?=\.)/gm, 'じゃ')
		.replace(/(だ{1})(?=\,)/gm, 'じゃ')
		.replace(/(だ{1})(?=\!)/gm, 'じゃ')*/
		.replace(/のだ$/gm, 'のじゃ')
		.replace(/(のだ{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'のじゃ')
		/*.replace(/(のだ{1})(?=、)/gm, 'のじゃ')
		.replace(/(のだ{1})(?=\！)/gm, 'のじゃ')
		.replace(/(のだ{1})(?=\.)/gm, 'のじゃ')
		.replace(/(のだ{1})(?=\,)/gm, 'のじゃ')
		.replace(/(のだ{1})(?=\!)/gm, 'のじゃ')*/
		.replace(/かな$/gm, 'かの')
		.replace(/(かな{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'かの')
		/*.replace(/(かな{1})(?=、)/gm, 'かの')
		.replace(/(かな{1})(?=\.$)/gm, 'かの')
		.replace(/(かな{1})(?=\,)/gm, 'かの')
		.replace(/(かな{1})(?=\?)/gm, 'かの')
		.replace(/(かな{1})(?=\？)/gm, 'かの')
		.replace(/(かな{1})(?=\!)/gm, 'かの')
		.replace(/(かな{1})(?=\！)/gm, 'かの')*/
		.replace(/だな$/gm, 'じゃの')
		.replace(/(だな{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'じゃの')
		/*.replace(/(だな{1})(?=、)/gm, 'じゃの')
		.replace(/(だな{1})(?=\.)/gm, 'じゃの')
		.replace(/(だな{1})(?=\,)/gm, 'じゃの')
		.replace(/(だな{1})(?=\?)/gm, 'じゃの')
		.replace(/(だな{1})(?=\？)/gm, 'じゃの')
		.replace(/(だな{1})(?=\!)/gm, 'じゃの')
		.replace(/(だな{1})(?=\！)/gm, 'じゃの')*/
		.replace(/なの$/gm, 'なのじゃ')
		.replace(/(なの{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'なのじゃ')
		/*.replace(/(なの{1})(?=、)/gm, 'なのじゃ')
		.replace(/(なの{1})(?=\！)/gm, 'なのじゃ')
		.replace(/(なの{1})(?=\.)/gm, 'なのじゃ')
		.replace(/(なの{1})(?=\,)/gm, 'なのじゃ')
		.replace(/(なの{1})(?=\!)/gm, 'なのじゃ')*/
		.replace(/だぞ$/gm, 'じゃぞ')
		.replace(/(だぞ{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'じゃぞ')
		.replace(/だが$/gm, 'じゃが')
		.replace(/(だが{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜|な|の)/gm, 'じゃが')
		.replace(/(きみ$|あなた$|おまえ$|お前$)/gm, 'おぬし')
		.replace(/(きみ{1}|あなた{1}|おまえ{1}|お前{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'おぬし')
		.replace(/(きみたち$|君達$|君たち$|あなたたち$|貴方達$|貴方たち$|あなた達$|おまえたち$|お前達$|お前たち$|おまえら$|お前ら$)/gm, 'おぬしら')
		.replace(/(きみたち{1}|君達{1}|君たち{1}|あなたたち{1}|貴方達{1}|貴方たち{1}|あなた達{1}|おまえたち{1}|お前達{1}|お前たち{1})(?=。|\.|、|\,|\！|\!|\?|\？|\…|\・|\‥|\ー|\～|\〜)/gm, 'おぬしら');
}
